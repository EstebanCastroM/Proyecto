    private void btnAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltaActionPerformed
        // TODO add your handling code here:
      objPro.setNombre(txtNombre.getText());
      objPro.setTipo(txtTipo.getText());
      objPro.setCodigo(txtCodigo.getText());
      objPro.setExistencia(Integer.parseInt(txtExistencia.getText()));
      objPro.setPrecio(Double.parseDouble(txtPrecio.getText()));
        BD.abrir();
        BD.insertarRegistro(objPro);
        BD.cerrar();
        
      JOptionPane.showMessageDialog(this, "Los Datos se almacenaron correctamente");
      limpiar_cajas();
      
    }//GEN-LAST:event_btnAltaActionPerformed

