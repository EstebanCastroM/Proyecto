    private void btnBuscarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProductoActionPerformed
        // TODO add your handling code here:
        BD.abrir();
        objPro=BD.consultarRegistron(txtProducto.getText());
        if(objPro !=null){
            txtMostrar.setText(objPro.mostrar_info());
            txtProducto.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "El Producto a buscar no existe");
        }
        BD.cerrar();

    }//GEN-LAST:event_btnBuscarProductoActionPerformed

