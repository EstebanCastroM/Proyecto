    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        // TODO add your handling code here:
        BD.abrir();
        objPro=BD.consultarRegistro(txtProductoMod.getText());
        if(objPro !=null){
        objPro.setPrecio(Double.parseDouble(txtPrecioMod.getText()));
        BD.actualizarRegistro(objPro);    
        JOptionPane.showMessageDialog(this, "Se ha Actualizado el Precio");
        txtProductoMod.setText("");
        txtPrecioMod.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "El Producto a buscar no existe");
        }
        BD.cerrar();
    }//GEN-LAST:event_btnModificarProductoActionPerformed

