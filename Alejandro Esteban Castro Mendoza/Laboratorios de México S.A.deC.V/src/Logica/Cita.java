package Logica;

/**
 *
 * @author EAV
 */
public class Cita {
    public String codigo12;
    public String dia;
    public String mes;
    public String hora;
    public String tipo;

    public String getCodigo12() {
        return codigo12;
    }

    public void setCodigo12(String codigo) {
        this.codigo12 = codigo;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
